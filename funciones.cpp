#include "funciones.h"
#include "math.h"
double suma (double a,double b){
     return a + b ;
}


double producto (double a,double b){
     return a * b ;
}

double division (double a,double b){
     return b / a ;
}

double potencia (double a,double b){
     return pow(b, a) ;
}

double seno (double a,double){
    return sin(a);
}

double coseno (double a,double){
    return cos(a);
}
